package it.unibo.jetpackjoyride.core.api;

/**
 * Enum to get the position of the gadget information.
 * 
 * @author lorenzo.bacchini4@studio.unibo.it
 */
public enum GadgetInfoPositions {
    /**
     * The state of the gadget.
     * active or not.
     */
    STATE,
    /**
     * The state of the gadget.
     * purchased or not.
     */
    PURCHASED,
    /**
     * The price of the gadget.
     */
    PRICE,
    /**
     * The description of the gadget.
     */
    DESCRIPTION
}
